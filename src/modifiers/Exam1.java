package modifiers;

public class Exam1 {
	private int a=1;
	int b=2;
	protected int c=3;
	public int d=4;


	public void display() {
		System.out.println("private variables is"+a);
		System.out.println("default variable is"+b);
		System.out.println("protected variable is"+c);
		System.out.println("public variable is"+d);



	}

}
