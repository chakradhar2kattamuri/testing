package oops;

public class Encapsulations {

	private String username;
	private String Password;

	public void setmailid(String uname) {
		username=uname;

	}

	public String getmailid() {
		return username;

	}
	public void setpassword(String pasword) {
		Password=pasword;

	}
	public String getpassword () {
		return Password;


	}
}
